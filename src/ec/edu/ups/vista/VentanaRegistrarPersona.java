    public void llenarTablaPersonas() {

        DefaultTableModel tablaP = (DefaultTableModel) tblPersona.getModel();

        tablaP.setRowCount(0);

        for (Persona p : personaDAO.findAll()) {
            Object[] rowData = { p.getCedula(),p.getNombre(),p.getApellido()};
            tablaP.addRow(rowData);
            tblPersona.setModel(tablaP);
        }

    }
    
